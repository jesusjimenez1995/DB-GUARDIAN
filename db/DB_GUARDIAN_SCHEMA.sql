CREATE TABLE INCIDENCIAS (
  ID               NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  APP_NAME         VARCHAR2(120) NOT NULL,
  ERROR_CODE       VARCHAR2(120) NOT NULL,
  TITULO           VARCHAR2(300) NOT NULL,
  DESCRIPCION      CLOB,
  ESTADO           VARCHAR2(30)  DEFAULT ''NUEVA''  NOT NULL,
  PRIORIDAD        VARCHAR2(10)  DEFAULT ''MEDIA''  NOT NULL,
  ASIGNADO_A       VARCHAR2(100),
  SOLUCION         CLOB,
  FECHA_ALTA       TIMESTAMP     DEFAULT SYSTIMESTAMP NOT NULL,
  FECHA_RESOLUCION TIMESTAMP,
  CONSTRAINT CK_INCIDENCIAS_ESTADO    CHECK (ESTADO    IN (''NUEVA'',''ASIGNADA'',''EN INVESTIGACION'',''ESCALADA'',''SOLUCIONADA'',''CERRADA'')),
  CONSTRAINT CK_INCIDENCIAS_PRIORIDAD CHECK (PRIORIDAD IN (''ALTA'',''MEDIA'',''BAJA''))
);

CREATE TABLE PLAYBOOKS (
  ID                 NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  ERROR_MATCH        VARCHAR2(300) NOT NULL,
  PROCEDIMIENTO_CLOB CLOB          NOT NULL,
  LINKS_INTERNOS     VARCHAR2(2000)
);

CREATE TABLE HISTORIAL_ESTADOS (
  ID              NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  INCIDENCIA_ID   NUMBER        NOT NULL REFERENCES INCIDENCIAS(ID) ON DELETE CASCADE,
  ESTADO_ANTERIOR VARCHAR2(30),
  ESTADO_NUEVO    VARCHAR2(30)  NOT NULL,
  COMENTARIO      VARCHAR2(1000),
  USUARIO         VARCHAR2(100),
  FECHA_CAMBIO    TIMESTAMP     DEFAULT SYSTIMESTAMP NOT NULL
);

CREATE INDEX IX_INCIDENCIAS_ESTADO    ON INCIDENCIAS (ESTADO);
CREATE INDEX IX_INCIDENCIAS_APP       ON INCIDENCIAS (APP_NAME);
CREATE INDEX IX_INCIDENCIAS_PRIORIDAD ON INCIDENCIAS (PRIORIDAD);
CREATE INDEX IX_PLAYBOOKS_ERROR_MATCH ON PLAYBOOKS (ERROR_MATCH);
CREATE INDEX IX_HISTORIAL_INCIDENCIA  ON HISTORIAL_ESTADOS (INCIDENCIA_ID);

CREATE OR REPLACE TRIGGER TRG_INCIDENCIAS_STATUS_FLOW
  BEFORE UPDATE OF ESTADO ON INCIDENCIAS
  FOR EACH ROW
DECLARE
  v_valid NUMBER := 0;
BEGIN
  IF :OLD.ESTADO = :NEW.ESTADO THEN RETURN; END IF;

  SELECT CASE
    WHEN :OLD.ESTADO = ''NUEVA''            AND :NEW.ESTADO = ''ASIGNADA''         THEN 1
    WHEN :OLD.ESTADO = ''ASIGNADA''         AND :NEW.ESTADO = ''EN INVESTIGACION''  THEN 1
    WHEN :OLD.ESTADO = ''EN INVESTIGACION'' AND :NEW.ESTADO = ''ESCALADA''          THEN 1
    WHEN :OLD.ESTADO = ''ESCALADA''         AND :NEW.ESTADO = ''SOLUCIONADA''       THEN 1
    WHEN :OLD.ESTADO = ''SOLUCIONADA''      AND :NEW.ESTADO = ''CERRADA''           THEN 1
    ELSE 0
  END INTO v_valid FROM dual;

  IF v_valid = 0 THEN
    RAISE_APPLICATION_ERROR(-20001, ''Invalid status transition: '' || :OLD.ESTADO || '' -> '' || :NEW.ESTADO);
  END IF;

  IF :NEW.ESTADO IN (''SOLUCIONADA'',''CERRADA'') THEN
    :NEW.FECHA_RESOLUCION := SYSTIMESTAMP;
  END IF;

  INSERT INTO HISTORIAL_ESTADOS (INCIDENCIA_ID, ESTADO_ANTERIOR, ESTADO_NUEVO, COMENTARIO)
  VALUES (:OLD.ID, :OLD.ESTADO, :NEW.ESTADO, ''Transicion automatica via API'');
END;
